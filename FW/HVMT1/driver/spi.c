#include "SPI.h"


/*///////////////////////////////////////////////////////
// SPI WRITE DATA
// WRITEDATA(8BIT)
///////////////////////////////////////////////////////*/
uint8_t writeSpi(uint8_t byte)
{
	SPDR = byte;
	while(!(SPSR&(1<<SPIF))); // SPIF가 1로 될때까지 대기. (SPI 전송이 완료될때까지 대기)
	return SPDR;
}